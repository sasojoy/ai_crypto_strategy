

import json
import re
import os

def extract_features():
    features_path = '/workspace/ai_crypto_strategy/src/core/features.py'
    with open(features_path, 'r') as f:
        content = f.read()
    
    # Extract MASTER_FEATURES list using regex
    match = re.search(r'MASTER_FEATURES = \[(.*?)\]', content, re.DOTALL)
    if match:
        features_str = match.group(1)
        features = re.findall(r"'(.*?)'", features_str)
        return features
    return []

def extract_max_depth():
    trainer_path = '/workspace/ai_crypto_strategy/optimize/trainer.py'
    with open(trainer_path, 'r') as f:
        content = f.read()
    
    # Look for max_depth suggestion range
    match = re.search(r"'max_depth': trial\.suggest_int\('max_depth', (\d+), (\d+)\)", content)
    if match:
        return f"{match.group(1)} - {match.group(2)}"
    return "Unknown"

def main():
    print("📦 Packing Audit Log...")
    
    # 1. Read backtest report
    report_path = '/workspace/ai_crypto_strategy/backtest_report.json'
